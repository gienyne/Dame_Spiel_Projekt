package Dame.Model;

/**
 * verwaltet die verschiedenen Zustände des Spiels
 */
public enum Gamestate {

    /**
     * stellt den Anfangszustand des Spiels dar
     */
    START,

    /**
     * steht für den Zustand während des Spiels
     */
    PLAYING,

    /**
     * stellt den EndZustand des Spiels
     */
    GAME_OVER

};
